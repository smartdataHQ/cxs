resolve_abs_path() {
  local path="$1"
  if command -v python3 >/dev/null 2>&1; then
    python3 - "$path" <<'PY'
import os, sys
print(os.path.abspath(sys.argv[1]))
PY
    return 0
  fi
  if [ -d "$path" ]; then
    (cd "$path" && pwd)
  else
    local dir
    dir="$(dirname "$path")"
    local base
    base="$(basename "$path")"
    (cd "$dir" >/dev/null 2>&1 && printf '%s/%s\n' "$(pwd)" "$base") || return 1
  fi
}

read_env_value() {
  local file="$1"
  local key="$2"
  if [ ! -f "$file" ]; then
    return 0
  fi
  if command -v python3 >/dev/null 2>&1; then
    python3 - "$file" "$key" <<'PY'
import sys
from pathlib import Path
path = Path(sys.argv[1])
key = sys.argv[2]
value = ""
for raw in path.read_text(encoding="utf-8").splitlines():
    line = raw.strip()
    if not line or line.startswith('#') or '=' not in line:
        continue
    k, v = line.split('=', 1)
    if k.strip() == key:
        v = v.strip()
        if len(v) >= 2 and v[0] == v[-1] and v[0] in "\"'":
            v = v[1:-1]
        value = v
        break
print(value, end="")
PY
  else
    local line
    line=$(grep -E "^${key}=" "$file" | tail -n1 || true)
    line="${line#${key}=}"
    line="${line%%$'\r'}"
    if [[ "${line}" =~ ^".*"$ ]] || [[ "${line}" =~ ^'.*'$ ]]; then
      line="${line:1:${#line}-2}"
    fi
    printf '%s' "$line"
  fi
}

# Generate random password/key
generate_random() {
  local length="${1:-16}"
  if command -v openssl >/dev/null 2>&1; then
    openssl rand -base64 "$length" | tr -d '\n'
  else
    # Fallback using /dev/urandom
    head -c "$length" /dev/urandom | base64 | tr -d '\n'
  fi
}

# Prompt for a secret with description and optional default
prompt_secret() {
  local var_name="$1"
  local description="$2"
  local default_value="$3"
  local is_required="$4"
  local value=""
  
  echo
  echo "ðŸ“‹ $var_name: $description"
  if [ -n "$default_value" ]; then
    if [[ "$default_value" == "AUTO_GENERATE"* ]]; then
      local gen_length="${default_value#AUTO_GENERATE:}"
      gen_length="${gen_length:-16}"
      default_value=$(generate_random "$gen_length")
      echo "   Auto-generated: $default_value"
    fi
    echo "   Default: $default_value"
  fi
  
  if [ "$is_required" = "true" ]; then
    echo "   (REQUIRED)"
  else
    echo "   (OPTIONAL - press Enter to skip)"
  fi
  
  printf "   Enter value: "
  read -r value
  
  if [ -z "$value" ]; then
    if [ -n "$default_value" ]; then
      value="$default_value"
    elif [ "$is_required" = "true" ]; then
      echo "   âŒ This field is required. Please enter a value."
      prompt_secret "$var_name" "$description" "$default_value" "$is_required"
      return
    fi
  fi
  
  # Validate based on var type
  case "$var_name" in
    DOCKER_PAT)
      if [[ ! "$value" =~ ^(ghp_|dckr_) ]] && [ -n "$value" ]; then
        echo "   âš ï¸  Warning: Docker PAT should start with 'ghp_' or 'dckr_'"
      fi
      ;;
    *_PASSWORD)
      if [ ${#value} -lt 8 ] && [ -n "$value" ]; then
        echo "   âš ï¸  Warning: Password should be at least 8 characters"
      fi
      ;;
    OPENAI_API_KEY)
      if [[ ! "$value" =~ ^sk- ]] && [ -n "$value" ]; then
        echo "   âš ï¸  Warning: OpenAI API key should start with 'sk-'"
      fi
      ;;
  esac
  
  # Set global variable for caller to use
  PROMPT_VALUE="$value"
}

