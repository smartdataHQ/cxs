import json
import os
import sys
from pathlib import Path
from urllib import request, error

owner = os.environ['OWNER']
repo = os.environ['REPO']
path = os.environ['GH_PATH']
dest = Path(os.environ['DEST'])
ref = os.environ.get('GH_REF', '')
